package com.nf147.entity;

public class Inventories {
    private Integer inventories_id;

    private Integer inventories_quantity;

    private Integer pet_id;

    public Integer getInventories_id() {
        return inventories_id;
    }

    public void setInventories_id(Integer inventories_id) {
        this.inventories_id = inventories_id;
    }

    public Integer getInventories_quantity() {
        return inventories_quantity;
    }

    public void setInventories_quantity(Integer inventories_quantity) {
        this.inventories_quantity = inventories_quantity;
    }

    public Integer getPet_id() {
        return pet_id;
    }

    public void setPet_id(Integer pet_id) {
        this.pet_id = pet_id;
    }
}